package knowledge;

public class DataType {

	public static void main(String[] args) {

		final double PI = 3.14; // define constant
		System.out.print("abcd");
		System.out.println("welcome");
		System.out.println("float=" + 1.0F / 3.0F); // float value
		System.out.println("double=" + 1.0 / 3.0); // double is more accurate from float thats why constant in double.

		System.out.println(PI);
		System.out.println("\u005c\u0022");
		System.out.println("a\\".length() + "\b".length());

		System.out.write('1');
	}

}
